-- mostra tudo das tabelas a seguir:
SELECT * FROM usuario_login AS Usuarios;
SELECT * FROM jogador;
SELECT * FROM atributo_jogador AS Atributos;

-- mostra email, senha e nome de todos os jogadores
SELECT
    ul.email_login AS email,
    ul.senha_login AS senha,
    j.nome_jogador AS nome
FROM usuario_login AS ul
INNER JOIN jogador AS j
ON ul.id_login = j.id_loginFK
ORDER BY ul.id_login;

-- mostra o jogador, o slot do inventario, o nome do item e a quantidade dele 
SELECT
    j.nome_jogador,
    inv.slot_inventario,
    it.nome_item,
    inv.quantidade_item
FROM jogador AS j
INNER JOIN inventario_jogador AS inv
ON j.id_jogador = inv.id_jogadorFK
INNER JOIN item AS it
ON it.id_item = inv.id_itemFK
ORDER BY j.id_jogador;

-- mostra o nome do jogador e seus atributos
SELECT
    j.nome_jogador AS nome,
    r.nome_raca AS raca,
    c.nome_classe AS classe,
    h.nome_habilidade AS habilidade,
    aj.hp_jogador AS hp,
    aj.xp_jogador AS xp,
    aj.forca_jogador AS forca,
    aj.foco_jogador AS foco,
    aj.reflexo_jogador AS reflexo,
    aj.hp_jogador AS mente
FROM jogador AS j
INNER JOIN atributo_jogador AS aj
ON j.id_jogador = aj.id_jogadorFK
INNER JOIN raca AS r
ON r.id_raca = aj.id_racaFK 
INNER JOIN classe AS c
ON c.id_classe = aj.id_classeFK
INNER JOIN habilidade AS h
ON h.id_habilidade = aj.id_habilidadeFK
ORDER BY id_jogador;

-- conta quantos jogadores tem de cada raca
SELECT
    r.nome_raca,
    COUNT(aj.id_jogadorFK) AS total_jogadores_por_raca
FROM raca AS r
JOIN atributo_jogador AS aj 
ON r.id_raca = aj.id_racaFK
GROUP BY r.nome_raca;         

-- conta quantos jogadores tem de cada classe
SELECT
    c.nome_classe,
    COUNT(aj.id_jogadorFK) AS total_jogadores_por_classe
FROM classe AS c
JOIN atributo_jogador AS aj 
ON c.id_classe = aj.id_classeFK
GROUP BY c.nome_classe