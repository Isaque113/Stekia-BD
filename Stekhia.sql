CREATE DATABASE stekhia;
USE stekhia;

CREATE TABLE usuario_login (
    id_login BIGINT PRIMARY KEY AUTO_INCREMENT,
    email_login VARCHAR(320) NOT NULL UNIQUE,
    senha_login VARCHAR(60) NOT NULL
);

CREATE TABLE checkpoint_mapa (
    id_checkpoint INT PRIMARY KEY AUTO_INCREMENT,
    nome_checkpoint VARCHAR(50),
    descricao_checkpoint VARCHAR(200)
);

CREATE TABLE jogador (
    id_jogador BIGINT PRIMARY KEY AUTO_INCREMENT,
    id_loginFK BIGINT NOT NULL,
    id_checkpointFK INT NOT NULL,
    nome_jogador VARCHAR(50) NOT NULL UNIQUE,
    status_conta_jogador VARCHAR(10),
    ultimo_login_jogador DATETIME,
    FOREIGN KEY (id_checkpointFK) REFERENCES checkpoint_mapa(id_checkpoint),
    FOREIGN KEY (id_loginFK) REFERENCES usuario_login(id_login)
);

CREATE TABLE item (
    id_item INT PRIMARY KEY AUTO_INCREMENT,
    nome_item VARCHAR(50),
    descricao_item VARCHAR(200)
);

CREATE TABLE raca (
    id_raca INT PRIMARY KEY AUTO_INCREMENT,
    nome_raca VARCHAR(50),
    descricao_raca VARCHAR(200)
);

CREATE TABLE classe (
    id_classe INT PRIMARY KEY AUTO_INCREMENT,
    nome_classe VARCHAR(50),
    descricao_classe VARCHAR(200)
);

CREATE TABLE habilidade (
    id_habilidade INT PRIMARY KEY AUTO_INCREMENT,
    nome_habilidade VARCHAR(30),
    descricao_classe VARCHAR(100)
);

CREATE TABLE atributo_jogador (
    id_atributo_jogador BIGINT PRIMARY KEY AUTO_INCREMENT,
    id_jogadorFK BIGINT,
    id_racaFK INT,
    id_classeFK INT,
    id_habilidadeFK INT,
    hp_jogador INT,
    xp_jogador INT,
    forca_jogador INT,
    foco_jogador INT,
    reflexo_jogador INT,
    mente_jogador INT,
    FOREIGN KEY (id_jogadorFK) REFERENCES jogador(id_jogador),
    FOREIGN KEY (id_racaFK) REFERENCES raca(id_raca),
    FOREIGN KEY (id_classeFK) REFERENCES classe(id_classe),
    FOREIGN KEY (id_habilidadeFK) REFERENCES habilidade(id_habilidade)
);

CREATE TABLE inventario_jogador (
    id_inventario BIGINT PRIMARY KEY AUTO_INCREMENT,
    id_jogadorFK BIGINT NOT NULL,
    id_itemFK INT,
    slot_inventario INT,
    quantidade_item INT,
    FOREIGN KEY (id_jogadorFK) REFERENCES jogador(id_jogador),
    FOREIGN KEY (id_itemFK) REFERENCES item(id_item)
);

CREATE TABLE npc (
    id_npc INT PRIMARY KEY AUTO_INCREMENT,
    nome_npc VARCHAR(50),
    descricao_npc VARCHAR(100)
);

CREATE TABLE amizade_jogador (
    id_amizade_npc VARCHAR(24) PRIMARY KEY,
    id_jogadorFK BIGINT,
    id_npcFK INT,
    pontos_amizade INT
);

